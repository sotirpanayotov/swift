
public class Task4_InchToCm {
    public static void main(String[] args) {

        final double x = 2.54;

        int a = 12;
        double b = 23.23;

        System.out.println(a + " inch " + "= " + a * x + " cm");

        double c = b * x;
        System.out.println(b + " inch " + "= " + (float)c + " cm");

    }
}

// 1 inch = 2.54 cm