
public class Task5_SwapVariables {
    public static void main(String[] args) {

        int a = 59;
        int b = a % 10;
        int c = a / 10;

        System.out.print(b);
        System.out.println(c);

        int e = 117;
        int d = e % 10;
        int f = e / 10;

        System.out.print(d);
        System.out.println(f);
    }
}

// 59
// 117